        <details>
            <summary>How is a forensic interview scheduled? </summary>
            <div><p>When a report of abuse is taken either by Law Enforcement or the Department of Children and Family Services (DCFS) they will contact the Children’s Advocacy Center and we will coordinate a date and time for the interview based on the availability of the child victim and their caregiver,  Law Enforcement, DCFS and the RI Co. State’s Attorney’s Office.  Parents, caregivers, or community members can not schedule an interview. </p>
            </div>
        </details>
        <details>
            <summary>
                Can a caregiver schedule an appointment for an interview? 
            </summary>
            <div>
                <p>No. Only Law Enforcement, DCFS, and/or the State’s Attorney’s office can make referrals for forensic interviews. </p>
            </div>
        </details>

        <details>
            <summary>How do I prepare my child for the visit to the Children’s Advocacy Center?</summary>
            <div><p>First and foremost, give your child permission to share their story with the interviewer. Letting them know that it is a safe and confidential space. </p></div>
        </details>
        <details>
            <summary>Will my child need a medical exam?</summary>
            <div><p>Oftentimes, if a child has experienced abuse whether physical or sexual, it is safe to have them be checked over by a medical professional. Depending on the allegations, a child may require a full forensic exam. The CAC can also refer children to a pediatric specialist to be examined. </p></div>
        </details>
        <details>
            <summary>Can parents or caregivers accompany a child into the interview? </summary>
            <div>
                <p>No. The interview will just be the trained forensic interviewer and child victim. This is protocol since the recorded interview is considered evidence. This keeps the chain of custody intact and prosecutors/defense attorneys will not be able to subpoena parents or caregivers to testify about the interview. </p>
            </div>
        </details>
        <details>
            <summary>
                What happens next? 
            </summary>
            <div><p>The forensic interview is the first step into the abuse investigation. After your child’s interview, your child will rejoin you in the waiting room. The investigators will meet with you(parent/caregiver) and discuss what next steps in your case will be. You will be provided with contact information for everyone involved in the case and assigned a family advocate that can assist you throughout the life of the case. </p></div>
        </details>
        <details>
            <summary>
                Does the Rock Island County Children’s Advocacy Center offer counseling or therapy services?
            </summary>
            <div><p>Yes. Our Center employs a full-time in house therapist. Our therapist specialized in trauma based therapy services. Services are available to children and their families seen here at the CAC. </p></div>
        </details>
        <details>
            <summary>How do I (parents/caregivers) get through this process? </summary>
            <div><p>This process is never an easy process. There is hope and there is assistance available. You aren’t in this alone. Our therapist will be available to schedule with you or your family advocate will be there to answer any questions you may have or to help with any resources you may need. </p></div>
        </details>
