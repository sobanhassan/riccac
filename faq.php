        <h2>Frequently Asked Questions</h2>
        <details>
          <summary>What is a Children’s Advocacy Center?</summary>
          <div>
              <p>A Children’s Advocacy Center (CAC) is a not-for-profit organization that facilitates child interviews for cases of extreme physical abuse and sexual abuse cases. Prior to CACs, children who disclosed abuse or were the subject of an investigation alleging abuse would have to recount their story, in detail, to several different entities. Sometimes retelling their story up to ten (10) different times. With the CAC, we have trained, trauma-informed forensic interviewers on staff to interview the children while all of the entities come together to view the child’s interview through a closed circuit camera system. Providing confidentiality and comfort allowing the child(ren) the space they need with minimal to no barriers when sharing their experiences.</p>
          </div>
        </details>
        <details>
          <summary>What if my child discloses abuse?</summary>
          <div>
              <p>The reaction that a child receives when they initially disclose will set the standard for their cooperation with the investigation. If they are doubted, interrogated, or disregarded they may shy away from any more disclosures. However, if met with positive regard and being believed, statistically they are more likely to participate in the investigation.</p>
