    <h1 align="center">Services  Provided</h1></p>
    <h2>Child  Friendly Facilities</h2>
    <p>Facilities to hold a coordinated investigation.</p>
    
    <h2>Forensic  Interview</h2><p>
        Interviews are conducted in a  manner that is legally sound and of a neutral, fact finding nature.&nbsp; All  children are interviewed by a person trained in forensic interviews. Interviews  are coordinated to avoid duplication.</p>
    <h2>Family  Advocacy/Case Management</h2><p>
        The Children’s Advocacy Center  maintains contact with the family throughout the life of their case. We work  cooperatively with agency service providers to make referrals based on the  needs of the child and their family.&nbsp; We track referrals and assist  families in connecting with service providers to initiate additional support.&nbsp;  The Children’s Advocacy Center assists the child victim and their family to  navigate the criminal justice system advocating for their concerns/needs with  multidisciplinary team members/service providers, provides education on the  investigative and court process, assists with resource attainment, and provides  emotional support.&nbsp;&nbsp;</p>
    <h2>Training</h2><p>
        We train our team members from law  enforcement; DCFS, State’s Attorney’s Office and other partners about child  abuse and investigative techniques.&nbsp; Training allows multidisciplinary  team members to develop and maintain specialized skills needed to better serve  child abuse survivors and their families. </p>
    <h2>Community  Outreach</h2><p>
        The Children’s Advocacy Center staff are  available to provide presentations on topics related to the Children’s Advocacy  Center concept and child abuse awareness.&nbsp; These efforts increase the  community’s awareness on the prevalence of child abuse as well as how to  identify and report abuse.&nbsp; Increased awareness and reporting leads to  more timely and successful intervention.</p>

        <h2>Therapy</h2>
        <p>Therapy services are provided free of charge to everyone who undergoes a Forensic Interview and their immediate family members. We have a full-time staff therapist who provides these services at the center. She is trained in Trauma Focused Cognitive Behavior Therapy and Eye Movement Desensitization and Reprocessing Therapy. Upon completion of therapy children are able to place their handprint on our new mural to signify moving forward in a positive light.</p>
        <p><a href="https://www.psychologytoday.com/us/therapy-types/trauma-focused-cognitive-behavior-therapy" target="_blank">More information about Trauma Focused Cognitive Behavior Therapy</a></p>
    <h2>Problematic Sexual Behaviour (PSB)</h2>
    <p>We have a group for children ages 7 -12 exhibiting problematic sexual behaviors. Group is 17 weeks for the children and their caregiver(s). Throughout the 17 weeks parents will learn how to parent a child exhibiting these behaviors and children will learn boundaries, empathy and responsibility for their behavior.</p>
    <p>The groups are led by our staff therapist and two other therapists we contract with. This program was developed to prevent future sexual abuse. We are optimistic that this group will prevent these children's behaviors from escalating. We are working with the local school districts, Law Enforcement, the Department of Children and Family Services and other community agencies for referrals.</p>
